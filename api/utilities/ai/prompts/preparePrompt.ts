const promptTemplate = `
Створи опис та назву товару українською для Shopify на основі:
1. Назва товару: "{{title}}".
2. Опис: "{{description}}".

**Вимоги**:
- Видали фрази про консультації, гарантію, "купуйте уважно" та відхилення.
- Опис має містити HTML (h2/h3/p/ul/li):
  - Перший блок — один короткий параграф з описом товару (без заголовка).
  - Другий блок - характеристики (при відсутньості скомпонуй з наявної інформації). При наявності інформації додай в характеристики пункти про виробника та p/n, при відсутньості ці пункти не потрібні.
  Для товарів "модулі пам'яті" зразок блоку характеристик:

Тип товару: Модуль оперативної пам'яті
Виробник: Hynix
Призначення: для ноутбуків
Об'єм пам'яті: 8 GB
Формфактор: SODIMM
Стандарт пам'яті: DDR4-2400/PC4-19200
Тип пам'яті: DDR4 SDRAM
Схема таймінгів: CL17
Частота пам'яті: 2400 MHz
Напруга живлення: 1,2 В

  - Третій (необов'язковий) блок - Комплектація.

**Результат**: JSON з полями:
1. **html**: оформлений опис.
2. **title**: коротка назва, включаюча код.
`;

export function preparePrompt(title: string, description: string): string {
  return promptTemplate
    .replace('{{title}}', title)
    .replace('{{description}}', description);
}
